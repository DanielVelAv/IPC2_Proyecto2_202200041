
class SistemaD:
    def __init__(self,nombre,alturamax,cantidadD,contenido):
        self.nombre = nombre
        self.alturamax = alturamax
        self.cantidadD = cantidadD
        self.contenido = contenido

    def getNombre(self):
        return self.nombre

    def getAlturaMax(self):
        return self.alturamax

    def getCantidadD(self):
        return self.cantidadD

    def getContenido(self):
        return self.contenido